#[cfg(test)]
mod tests {
    use super::*;

    fn make_fb(w: usize, h: usize) -> (Framebuffer, Vec<u8>) {
        let bpp    = 4;
        let stride = w * bpp;
        let len    = h * stride;
        let mut buf = vec![0u8; len];
        let ptr    = buf.as_mut_ptr();
        // SAFETY: buf is exclusively owned and lives as long as the test.
        let fb = Framebuffer::from_raw(ptr, len, w, h, stride, PixelFormat::Bgr);
        (fb, buf)
    }

    #[test]
    fn cols_and_rows_computed_correctly() {
        let (fb, _buf) = make_fb(800, 600);
        assert_eq!(fb.cols(), 800 / CHAR_W);
        assert_eq!(fb.rows(), 600 / CHAR_H);
    }

    #[test]
    fn write_byte_advances_cursor() {
        let (mut fb, _buf) = make_fb(320, 200);
        assert_eq!(fb.col, 0);
        fb.write_byte(b'A');
        assert_eq!(fb.col, 1);
    }

    #[test]
    fn newline_resets_column_increments_row() {
        let (mut fb, _buf) = make_fb(320, 200);
        fb.write_byte(b'A');
        fb.write_byte(b'\n');
        assert_eq!(fb.col, 0);
        assert_eq!(fb.row, 1);
    }

    #[test]
    fn wrap_at_end_of_line() {
        let (mut fb, _buf) = make_fb(CHAR_W * 4, CHAR_H * 4); // 4 cols
        for _ in 0..4 { fb.write_byte(b'X'); }
        // col == cols → next write triggers newline first
        fb.write_byte(b'X');
        assert_eq!(fb.row, 1);
        assert_eq!(fb.col, 1);
    }

    #[test]
    fn scroll_when_last_row_reached() {
        let rows = 3usize;
        let (mut fb, _buf) = make_fb(CHAR_W * 4, CHAR_H * rows);
        // Fill all rows.
        for _ in 0..rows {
            fb.write_byte(b'\n');
        }
        // Should have scrolled; row stays at rows-1.
        assert_eq!(fb.row, rows - 1);
    }

    #[test]
    fn clear_resets_cursor() {
        let (mut fb, _buf) = make_fb(320, 200);
        fb.write_byte(b'A');
        fb.write_byte(b'\n');
        fb.clear();
        assert_eq!(fb.col, 0);
        assert_eq!(fb.row, 0);
    }

    #[test]
    fn glyph_draws_foreground_pixel() {
        // 'A' (0x41) has bit 7 of row 1 set (0x18 → bits 3,4 set at columns 3,4).
        // Row 1 of A glyph = 0x3C = 0011_1100 → pixels 2,3,4,5 lit.
        let (mut fb, buf) = make_fb(CHAR_W, CHAR_H);
        fb.write_byte(b'A');
        // Find at least one foreground-coloured pixel in the buffer.
        let fg_found = buf.chunks(4).any(|px| {
            // BGR: b=FG.2, g=FG.1, r=FG.0
            px[0] == FG.2 && px[1] == FG.1 && px[2] == FG.0
        });
        assert!(fg_found, "no foreground pixels written for 'A'");
    }

    #[test]
    fn pixel_format_rgb_writes_correctly() {
        let mut buf = [0u8; 4];
        PixelFormat::Rgb.write_pixel(&mut buf, 0xAA, 0xBB, 0xCC);
        assert_eq!(buf[0], 0xAA); // R
        assert_eq!(buf[1], 0xBB); // G
        assert_eq!(buf[2], 0xCC); // B
    }

    #[test]
    fn pixel_format_bgr_writes_correctly() {
        let mut buf = [0u8; 4];
        PixelFormat::Bgr.write_pixel(&mut buf, 0xAA, 0xBB, 0xCC);
        assert_eq!(buf[0], 0xCC); // B
        assert_eq!(buf[1], 0xBB); // G
        assert_eq!(buf[2], 0xAA); // R
    }

    #[test]
    fn font_table_has_correct_size() {
        assert_eq!(FONT.len(), 95);
        assert_eq!(FONT[0].len(), CHAR_H);
    }
}
