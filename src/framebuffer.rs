//! Pixel framebuffer text renderer.
//!
//! ## Overview
//!
//! [`Framebuffer`] writes text into a linear pixel buffer provided by the
//! bootloader (`BootInfo::framebuffer`).  It maintains a software cursor and
//! scrolls the whole screen up by one line when the cursor reaches the bottom.
//!
//! Text is rendered using a built-in 8×16 monospaced bitmap font covering
//! printable ASCII (0x20–0x7E).  Non-printable bytes are replaced with `?`.
//!
//! ## Pixel format
//!
//! The bootloader reports the pixel format at runtime.  We support:
//! - `Rgb`  — R G B (3 or 4 bytes/pixel depending on stride)
//! - `Bgr`  — B G R
//! - `U8`   — greyscale (one byte per pixel)
//! - `Unknown` — treated as BGR
//!
//! ## Thread safety
//!
//! All public access goes through `FRAMEBUFFER: spin::Mutex<Option<Framebuffer>>`.
//!
//! ## Host unit-test strategy
//!
//! The font bitmaps and glyph rendering logic are exercised on the host using
//! a synthetic `[u8]` buffer.  The `bootloader_api` types are only used in the
//! `init` function which is gated to `#[cfg(target_os = "none")]`.

